public class Numbers {
    public static void main(String[] Args) {
        int newNumber = 54556;
        System.out.println("Введенное число: " + newNumber);
        if (newNumber % 10 == 7) {
            System.out.println("Последнее число семь");
        } else
            System.out.println("Последнее число не является семеркой");
    }
}
